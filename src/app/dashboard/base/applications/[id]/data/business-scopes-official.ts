/**
 * 根据关键词搜索经营范围
 */
export function searchBusinessScopes(keyword: string, limit = 20): BusinessScope[] {
  if (!keyword.trim()) return [];
  const lowerKeyword = keyword.toLowerCase();
  return BUSINESS_SCOPES.filter(
    scope => scope.name.toLowerCase().includes(lowerKeyword) || 
             scope.id.toLowerCase().includes(lowerKeyword)
  ).slice(0, limit);
}

/**
 * 根据行业大类获取经营范围
 */
export function getScopesByCategory(categoryCode: string): BusinessScope[] {
  return BUSINESS_SCOPES.filter(scope => scope.categoryCode === categoryCode);
}

/**
 * 根据ID获取经营范围
 */
export function getScopeById(id: string): BusinessScope | undefined {
  return BUSINESS_SCOPES.find(scope => scope.id === id);
}
